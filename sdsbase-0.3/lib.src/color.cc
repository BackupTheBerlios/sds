int maxcolor(void) {
	return sizeof(colors)/sizeof(RGBColor);
}

int color_ind(const char *name)
{
	int i;
	int max=maxcolor();
	for(i=0;i<max;i++) {
		if(!strcmp(name,colors[i].name)) {
			return i;
		}
	}
	return -1;
}

const char *color_name(int ind)
{
	int max=maxcolor();
	if(ind>=0&&ind<max)
		return colors[ind].name;
	return NULL;
}
